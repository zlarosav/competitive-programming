using namespace std;

void solve() {
  int n; cin >> n;
  int cua = sqrt(n), cub = cbrt(n), six = pow(n, 1.0/6.0);
  cout << cua + cub - six << '\n';
}

int main() {
  ios_base::sync_with_stdio(0);
  cin.tie(0);

  int t = 1;
  cin >> t;
  while (t--) {
    solve();
  }

  return 0;
}

